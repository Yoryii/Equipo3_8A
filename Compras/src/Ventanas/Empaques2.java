    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (editando) {//editar
            if (txtNombre.getText().isEmpty() || txtCapacidad.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Por favor rellene todos los campos");
            } else {
                int id = Integer.parseInt(txtId.getText());
                String nombre = txtNombre.getText();
                String capacidad = txtCapacidad.getText();
                String estatus = "A";
                //Sacar idUnidad inicio
                int idUnidad = 1;
                String unidad = (String) cmbUnidad.getSelectedItem();

                try {

                    PreparedStatement ps;
                    ResultSet rs;
                    Connection con = Conexion.getConexion();
                    ps = con.prepareStatement("SELECT idUnidad FROM UnidadesMedida WHERE nombre=?");
                    ps.setString(1, unidad);
                    rs = ps.executeQuery();
                    while (rs.next()) {
                        idUnidad = rs.getInt("idUnidad");
                    }

                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, e.toString());
                }
                //Sacar idUnidad fin
                try {

                    Connection con = Conexion.getConexion();
                    PreparedStatement ps = con.prepareStatement("UPDATE Empaques SET nombre=?, capacidad=?, estatus=?, idUnidad=? WHERE idEmpaque=?");
                    ps.setString(1, nombre);
                    ps.setString(2, capacidad);
                    ps.setString(3, estatus);
                    ps.setInt(4, idUnidad);
                    ps.setInt(5, id);
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Registro modificado con exito");
                    Limpiar();
                    cargarTabla();

                } catch (SQLException e) {

                    JOptionPane.showMessageDialog(null, e.toString());

                }
            }
        } else {//guardar
            if (txtNombre.getText().isEmpty() || txtCapacidad.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Por favor rellene todos los campos");
            } else {
                String nombre = txtNombre.getText();
                String capacidad = txtCapacidad.getText();
                String estatus = "A";
                //Sacar idUnidad inicio
                int idUnidad = 1;
                String unidad = (String) cmbUnidad.getSelectedItem();

                try {

                    PreparedStatement ps;
                    ResultSet rs;
                    Connection con = Conexion.getConexion();
                    ps = con.prepareStatement("SELECT idUnidad FROM UnidadesMedida WHERE nombre=?");
                    ps.setString(1, unidad);
                    rs = ps.executeQuery();
                    while (rs.next()) {
                        idUnidad = rs.getInt("idUnidad");
                    }

                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, e.toString());
                }
                //Sacar idUnidad fin
                //inicio
                try {

                    Connection con = Conexion.getConexion();
                    PreparedStatement ps = con.prepareStatement("INSERT INTO Empaques "
                            + "(nombre, capacidad, estatus, idUnidad) VALUES (?,?,?,?)");
                    ps.setString(1, nombre);
                    ps.setString(2, capacidad);
                    ps.setString(3, estatus);
                    ps.setInt(4, idUnidad);
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Registro guardado con exito");
                    Limpiar();
                    cargarTabla();

                } catch (SQLException e) {

                    JOptionPane.showMessageDialog(null, e.toString());

                }
                //fin
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

