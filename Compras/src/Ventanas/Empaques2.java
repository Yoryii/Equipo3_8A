    private void tblEmpaquesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmpaquesMouseClicked
        try {

            int fila = tblEmpaques.getSelectedRow();
            int id = Integer.parseInt(tblEmpaques.getValueAt(fila, 0).toString());
            PreparedStatement ps;
            ResultSet rs;

            Connection con = Conexion.getConexion();
            editando = true;
            activarBotones();
            ps = con.prepareStatement("SELECT E.nombre, E.capacidad, U.nombre AS Unidad FROM Empaques AS E JOIN UnidadesMedida AS U ON E.idUnidad = U.idUnidad WHERE idEmpaque=?");
            ps.setInt(1, id);
            rs = ps.executeQuery();
            while (rs.next()) {
                txtId.setText(String.valueOf(id));
                txtNombre.setText(rs.getString("nombre"));
                txtCapacidad.setText(rs.getString("capacidad"));
                cmbUnidad.setSelectedItem(rs.getString("Unidad"));
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_tblEmpaquesMouseClicked

